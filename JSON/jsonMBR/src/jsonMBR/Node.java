package jsonMBR;

public class Node {
	private int level;
	private int entry;
	private double lx, ly, ux, uy;
	
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getEntry() {
		return entry;
	}
	public void setEntry(int entry) {
		this.entry = entry;
	}
	public double getLx() {
		return lx;
	}
	public void setLx(double lx) {
		this.lx = lx;
	}
	public double getLy() {
		return ly;
	}
	public void setLy(double ly) {
		this.ly = ly;
	}
	public double getUx() {
		return ux;
	}
	public void setUx(double ux) {
		this.ux = ux;
	}
	public double getUy() {
		return uy;
	}
	public void setUy(double uy) {
		this.uy = uy;
	}
	
	
	
}
